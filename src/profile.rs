pub fn torch_var<S>(name: &str, shape: S) -> String
where
    S: ToShape,
{
    let shape = shape.to_shape(0);
    format!("{} = torch.randn(({})).cuda()\n", name, shape.to_string2())
}

pub fn torch_del_var(name: &str) -> String {
    format!("del {}\n", name)
}

pub struct Profiler {
    comp_time: HashMap<String, usize>,
    benchmark: HashMap<String, (String, String, String)>,
}

impl Profiler {
    pub fn new() -> Self {
        Profiler {
            comp_time: HashMap::new(),
            benchmark: HashMap::new(),
        }
    }

    pub fn add_benchmark(
        &mut self,
        uid: &str,
        prep_code: String,
        exec_code: String,
        clear_code: String,
    ) {
        self.benchmark
            .insert(uid.to_string(), (prep_code, exec_code, clear_code));
    }

    pub fn comp_time(&self, uid: &str) -> Option<usize> {
        self.comp_time.get(uid).cloned()
    }

    pub fn gen_benchmark(&self, repeat: usize) -> String {
        // step 1. profiler code

        let mut code = String::new();

        let indent = "    ";

        code.push_str("import timeit\n");
        code.push_str("import torch\n");
        code.push_str("from tqdm import tqdm\n");

        code.push_str(&format!("pbar = tqdm(total={})\n", self.benchmark.len()));
        code.push_str("benchmark = {}\n");
        code.push_str(&format!("for i in range({}):\n", repeat));

        for (uid, (prep_code, exec_code, clear_code)) in self.benchmark.iter() {
            // prep start
            code.push_str(indent);
            code.push_str(&prep_code.replace('\n', &format!("\n{}", indent)));

            // exec start
            code.push_str("\n");
            code.push_str(indent);
            code.push_str(&format!(
                "t = timeit.timeit(lambda: {}, number=1)\n",
                exec_code
            ));
            code.push_str(indent);
            code.push_str(&format!("benchmark.setdefault('{}', 0) \n", uid));
            code.push_str(indent);
            code.push_str(&format!("benchmark['{}'] += t\n", uid));

            // clear
            code.push_str("\n");
            code.push_str(indent);
            code.push_str(&clear_code.replace('\n', &format!("\n{}", indent)));
            code.push_str("torch.cuda.empty_cache()\n");

            code.push_str("\n");
            code.push_str(indent);
            code.push_str("pbar.update(1)\n");
        }
        code.push_str("pbar.close()\n");

        code.push_str("rust_code = '['\n");
        code.push_str("for uid in benchmark:\n");
        code.push_str(indent);
        code.push_str(&format!(
            "rust_code += '(\"' + uid + '\", ' + str(benchmark[uid] / {}) + '), '\n",
            repeat
        ));
        code.push_str("rust_code += ']'\n");
        code.push_str("f = open(\"out.txt\", \"w\")\n");
        code.push_str("f.write(rust_code)\n");
        code.push_str("f.close()\n");

        code
    }
}

